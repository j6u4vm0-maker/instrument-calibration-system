function flatten(obj: any, lang: Language, prefix = '') {
  let result: any = {};
  for (const key in obj) {
    const newKey = prefix ? `${prefix}_${key}` : key;
    // Check if the current level is a leaf node (contains the target language)
    if (obj[key] && obj[key][lang] !== undefined && typeof obj[key][lang] === 'string') {
      result[newKey] = obj[key][lang];
    } 
    // If it's a nested object, recurse
    else if (obj[key] && typeof obj[key] === 'object' && !obj[key][lang]) {
      Object.assign(result, flatten(obj[key], lang, newKey));
    }
  }
  return result;
}
